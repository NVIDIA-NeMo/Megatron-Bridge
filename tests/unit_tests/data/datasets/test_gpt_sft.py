def create_mock_tokenizer():
    """Create a mock tokenizer for testing without requiring actual tokenizer files."""
    mock_tokenizer = MagicMock()
    mock_tokenizer.eos_id = 2
    mock_tokenizer.bos_id = 1
    mock_tokenizer.pad_id = 0
    mock_tokenizer.vocab_size = 50000
    mock_tokenizer.space_sensitive = True

    def mock_text_to_ids(text):
        # Simple mock: return a list of token ids based on text length
        return list(range(1, len(text.split()) + 2))

    mock_tokenizer.text_to_ids = mock_text_to_ids
    return mock_tokenizer


def get_gpt_sft(tmp_path, dataset_type="sft", max_num_samples="default"):
    """Create a GPT SFT dataset for testing with mocked tokenizer.

    When ``max_num_samples`` is None the dataset builds no ``samples_mapping``,
    so physical indices are used directly and the batch sampler's ``-1`` padding
    rows survive to the autogenerated-sample path. This mirrors the finite-epoch
    GPT SFT builder, which builds datasets with ``max_num_samples=None``.
    """
    datasets_dir = tmp_path / "datasets"
    datasets_dir.mkdir(exist_ok=True)
    path = str(datasets_dir / "sft.jsonl")
    line = {"input": "hi how are you?", "output": "I'm fine, thanks."}

    # Enough rows for index checks (e.g. [11] and [-11]); keep small for fast IO and mapping.
    num_samples = 24
    with open(path, "w") as f:
        for i in range(num_samples):
            f.write(json.dumps(line) + "\n")

    tokenizer = create_mock_tokenizer()

    sft_max_num_samples = num_samples if max_num_samples == "default" else max_num_samples

    if dataset_type == "sft":
        dataset = GPTSFTDataset(
            file_path=path,
            tokenizer=tokenizer,
            label_key="output",
            max_num_samples=sft_max_num_samples,
            prompt_template="{input}\n\n### Response:\n{output}",
            truncation_field="output",
            memmap_workers=1,
        )
    elif dataset_type == "packed":
        # Create a mock packed dataset file
        packed_path = str(datasets_dir / "sft.npy")
        mock_packed_data = np.array(
            [
                {
                    "input_ids": np.array([1, 2, 3, 4, 5], dtype=np.int64),
                    "seq_start_id": [0],
                    "loss_mask": np.array([0, 0, 1, 1, 1], dtype=np.int64),
                }
                for _ in range(num_samples)
            ],
            dtype=object,
        )
        np.save(packed_path, mock_packed_data)
        dataset = GPTSFTPackedDataset(
            file_path=packed_path,
            tokenizer=tokenizer,
            label_key="output",
            max_num_samples=sft_max_num_samples,
            prompt_template="{input}\n\n### Response:\n{output}",
            truncation_field="output",
            memmap_workers=1,
        )
    else:
        dataset = GPTSFTChatDataset(
            file_path=path,
            tokenizer=tokenizer,
            label_key="output",
            prompt_template="{input}\n\n### Response:\n{output}",
            truncation_field="output",
            memmap_workers=1,
        )

    return dataset, num_samples


@pytest.mark.parametrize("mkdir_error", [FileExistsError, FileNotFoundError])
def test_get_dataset_root_ignores_shared_fs_mkdir_race(tmp_path, monkeypatch, mkdir_error):
    """Network filesystems can leak mkdir races even with exist_ok=True."""
    monkeypatch.setattr(gpt_sft_module, "NEMO_DATASETS_CACHE", tmp_path)

    def raise_mkdir(self, parents=False, exist_ok=False):
        assert self == tmp_path / "sft"
        assert parents is True
        assert exist_ok is True
        raise mkdir_error("stale shared filesystem state")

    monkeypatch.setattr(Path, "mkdir", raise_mkdir)

    assert get_dataset_root("sft") == tmp_path / "sft"


class TestDataGPTSFTDataset:
    def test_build_samples_mapping(self, tmp_path):
        dataset, _ = get_gpt_sft(tmp_path)
        dataset._build_samples_mapping()

    def test_gpt_sft_dataset(self, tmp_path):
        dataset, dataset_length = get_gpt_sft(tmp_path)

        assert len(dataset) == dataset_length
        assert type(dataset[11]) is dict
        assert type(dataset[-11]) is dict

    def test_separate_template(self, tmp_path):
        dataset, _ = get_gpt_sft(tmp_path)
        template_strings, template_strings_keys = dataset._separate_template(["output"])

        assert template_strings == ["output", "\n\n### Response:\n", "{output}"]
        assert template_strings_keys == ["input", "<template>", "output"]

    def test_multiple_truncation(self, tmp_path):
        dataset, _ = get_gpt_sft(tmp_path)

        template_ids = [
            [101, 102, 103, 104],
            [201, 202, 203],
            [301, 302],
        ]
        template_ids_keys = ["input", "<template>", "output"]
        context_ids, label_ids = dataset._multiple_truncation(template_ids, template_ids_keys)

        assert context_ids == [101, 102, 103, 104, 201, 202, 203]
        assert label_ids == [301, 302]

    def test_utils_func(self, tmp_path):
        dataset, _ = get_gpt_sft(tmp_path)

        assert dataset._truncation([101, 102, 103, 104], 0) == []
        assert dataset._truncation([101, 102, 103, 104], 2) == [101, 102]

        assert dataset._maybe_cast_to_list([1]) == [1]
        assert dataset._maybe_cast_to_list(np.array([1])) == [1]

        assert dataset._ceil_to_nearest(1, 2) == 2

        assert dataset._collate_item([[1, 2, 3, 4, 5]], 3, 0) == [[1, 2, 3, 4, 5]]

        processed_example = {"input_ids": [0, 1, 2, 11, 54], "answer_start_idx": 3}
        assert dataset._build_loss_mask(processed_example) == [0.0, 0.0, 0.0, 1.0, 1.0]

        dataset._create_attention_mask(3)

    def test_collate_fn(self, tmp_path):
        dataset, _ = get_gpt_sft(tmp_path)

        batch = [
            {
                "input_ids": [101, 102, 103, 104, 105],
                "context_ids": [101, 102],
                "answer_start_idx": 2,
                "context_length": 2,
                "answer_ids": [104, 105],
                "metadata": {"id": "ex1"},
                "token_count": 5,
            },
            {
                "input_ids": [201, 202, 203, 204],
                "context_ids": [201],
                "answer_start_idx": 1,
                "context_length": 1,
                "answer_ids": [203, 204],
                "metadata": {"id": "ex2"},
                "token_count": 4,
            },
        ]
        dataset.collate_fn(batch)

    def test_autogenerated_padding_sample_has_zero_loss_mask(self, tmp_path):
        # max_num_samples=None => no samples_mapping, so a negative index is treated
        # as an autogenerated padding row instead of being remapped to a real sample.
        dataset, _ = get_gpt_sft(tmp_path, max_num_samples=None)

        batch = dataset.collate_fn([dataset[0], dataset[-1]])

        assert batch["metadata"][1]["__AUTOGENERATED__"] is True
        assert batch["loss_mask"][1].sum().item() == 0

    def test_batch_sampler_padding_rows_do_not_contribute_to_loss(self, tmp_path):
        dataset, _ = get_gpt_sft(tmp_path, max_num_samples=None)
        global_batch_size = 5

        dataloader = build_pretraining_data_loader(
            dataset=dataset,
            consumed_samples=0,
            dataloader_type="batch",
            micro_batch_size=1,
            num_workers=0,
            data_sharding=False,
            collate_fn=dataset.collate_fn,
            pin_memory=False,
            data_parallel_rank=0,
            data_parallel_size=1,
            drop_last=False,
            global_batch_size=global_batch_size,
        )

        *_, final_batch = dataloader

        assert len(dataset) % global_batch_size != 0
        assert final_batch["metadata"][-1]["__AUTOGENERATED__"] is True
        assert final_batch["loss_mask"][-1].sum().item() == 0

    @pytest.mark.parametrize("dataset_type", ["sft", "packed"])
    def test_batch_sampler_mapped_padding_rows_do_not_contribute_to_loss(self, tmp_path, dataset_type):
        dataset, _ = get_gpt_sft(tmp_path, dataset_type=dataset_type)
        global_batch_size = 5

        dataloader = build_pretraining_data_loader(
            dataset=dataset,
            consumed_samples=0,
            dataloader_type="batch",
            micro_batch_size=1,
            num_workers=0,
            data_sharding=False,
            collate_fn=dataset.collate_fn,
            pin_memory=False,
            data_parallel_rank=0,
            data_parallel_size=1,
            drop_last=False,
            global_batch_size=global_batch_size,
        )

        *_, final_batch = dataloader

        assert dataset.samples_mapping is not None
        assert len(dataset) % global_batch_size != 0
        assert final_batch["loss_mask"][-1].sum().item() == 0


